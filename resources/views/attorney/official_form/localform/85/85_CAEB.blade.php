<div class="col-md-12 mt-3">
<h3>{{ __('2. Internet Connection') }}</h3>
<p class="mt-3">{{ __('The Electronic Filing System is accessible through an Internet site; therefore, Internet access is essential. To access the Internet, you will need the services and software of an Internet Service Provider (ISP).') }}</p>
<p class="mt-3">{{ __('Connection to the Internet for the purpose of electronic filing should be faster than a standard dial-up connection.1 Consequently, use of a dial-up modem to access the Internet for electronic filing purposes is not recommended.') }}</p>
<p class="mt-3">{{ __('The faster the Internet connection, the easier the electronic filing system will be to use. Electronic filers should, therefore, use DSL connections, cable modems, ISDN, or T1 lines to access the Internet.') }}</p>
</div>

<div class="col-md-12 mt-3">
<h3>{{ __('3. Web Browser') }}</h3>
<p class="mt-3">{{ __('The Electronic Filing System is a web application that operates on the Internet using a Web browser. Use of Internet Explorer 6.0 or higher is highly recommended. However, other browsers, such as Netscape 7.0 or higher, may also be functional. Browsers running on other operating systems, including Macintosh, have not been tested and may not be compatible.') }}</p>
</div>

<div class="col-md-12 mt-3">
<h3>{{ __('4. Document Preparation Software') }}</h3>
<p class="mt-3">a. <span class="underline">{{ __('A word processing application') }}</span> (such as Corel WordPerfect or Microsoft Word) <span class="underline">or a forms generation software package</span> {{ __('will be needed to create documents.') }}</p>
<p class="mt-3"><span class="underline">{{ __('b. Portable Document Format (PDF) writer software') }}</span> {{ __('(such as Adobe Acrobat PDF Writer) is required to convert electronic documents from a word processing format to PDF.') }}</p>
<p class="ml-30 mt-3">{{ __('Adobe Acrobat Version 6.0 (or higher) offers two packages for converting documents to PDF – Acrobat Distiller and Acrobat Writer. Version 6.0's standard installation automatically installs the Distiller package. We recommend, however, that e-filers using Adobe Acrobat 6.0's software opt for ‘custom’ installation and install Adobe Acrobat Writer 6.0 because it converts files to PDF considerably faster, and produces significantly smaller PDF files, than Adobe Acrobat Distiller. Subsequent versions of Adobe Acrobat install Adobe Writer by default.') }}</p>
<p class="mt-3">c. <span class="underline">{{ __('Zip file creation software') }}</span> {{ __('(such as WinZip) is required to electronically file batches of documents. Zip files are “archives” used for distributing and storing files. Zip files contain one or more files. Usually the files “archived” in a Zip file are compressed to save space. Zip files make it easy to group files and make transporting and copying these files faster.') }}</p>
</div>


<div class="col-md-12 mt-3">
<h3>{{ __('5. Scanner and Scanning Software') }}</h3>
<p class="ml-30 mt-3">{{ __('Documents that are not in a user’s computer in the form of word processing files (for example, a deed to secure a debt or a promissory note) will have to be scanned in order to file them electronically. Scanners range in price from $50 to tens of thousands of dollars. A flat bed scanner may be adequate for most users. If you anticipate imaging many documents, you may wish to consider a scanner with a sheet feeder attachment.') }}</p>
<p class="ml-30 mt-3">{{ __('The resolution of documents scanned for subsequent electronic filing must be 300 dpi (dots per inch). Because scanning at higher resolutions produces unnecessarily large image files, and scanning at lower resolutions produces poor quality images, scanning software must be capable of creating images in PDF format at exactly 300 dpi (dots per inch) resolution. Otherwise, software capable of converting the scanned document’s image file format to PDF is required.') }}</p>
</div>

