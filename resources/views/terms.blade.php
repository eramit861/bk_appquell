    <h3 class="text-center"><b>ADDITIONAL SERVICES</b></h3>
    <p>
        Additional Services currently include Payroll Assistant, as a subscription or one time use. Bank Statement Assistant, as a subscription or one time use. Paralegal Check. 3 Years storage on a separate server not on BK Assistant, Inc.’s premises through a third party currently with AWS, however the Law Firm agrees BK Assistant, Inc. may change this server and/or location or third-party company it currently uses at any time without prior notice or consent by the Law Firm. 
    </p>
    <p>
        <u>Payroll Assistant:</u> This service is provided by your client logging into their employer payroll company and your client(s) payroll company sharing your client(s) payroll data for up to the last 13 months with BK Assistant, Inc. by and through Aryle API. This service is an additional cost depending on the subscription you do or don’t choose starting at $19.99 (Nineteen dollars and ninety-nine cents USD).
    </p>
    <p>
        <u>Bank Statement Assistant:</u> This service is provided by your client logging into their bank accounts through our API with Mastercard/Finicity and your client(s) bank sharing your client(s) bank statements for the last 2 months with BK Assistant, Inc. by and through Mastercard/Finicity API. This service is an additional cost depending on the subscription you do or don’t choose starting at $19.99 (Nineteen dollars and ninety-nine cents USD).
    </p>
    <p>
        <u>Full Concierge Service:</u> This service is a representative of BK Assistant calls your client on a recorded line. The representative walks your client(s) through filling out the questionnaire and uploading the documents. By asking them the questions contained within the questionnaire selected by the attorney and input the information into the questionnaire for the client. By choosing this service you and your law firm agree to give BK Assistant, Inc. and its representatives access to your clients in order to provide this service. This service is an additional cost depending on the subscription you do or don’t choose starting at $50.00 (fifty dollars and zero cents USD).
    </p>
    <p>
        <u>Document Concierge Service:</u> This service is a representative of BK Assistant calls your client on a recorded line. The representative walks your client(s) through scanning in the documents in the Apple or Android apps that BK Assistant owns as BK Questionnaire.  By choosing this service you and your law firm agree to give BK Assistant, Inc. and its representatives access to your clients in order to provide this service. This service is an additional cost depending on the subscription you do or don’t choose starting at $8.99 (eight dollars and ninety-nine cents USD).
    </p>
    <p>
        <u>Paralegal Check:</u> This is an internal software algorithm designed by BK Assistant, Inc. This checks the petition based upon the client questionnaire to make sure you listed all items on Schedules A/B, C, D, E/F, I, and the Statement of financial affairs forms, if it is listed on one of the other forms and or the client questionnaire. This service is only included in three subscriptions (Premium, Premium Plus & Ultimate as part of the total price of the subscription you choose).
    </p>
    <p>
        <u>Import into any Preparation System:</u> In this service you pay to have one of BK Assistant’s representatives manually input the information from the client questionnaire into the preparation system you choose for this service. By choosing this service you and your law firm agree to give BK Assistant, Inc., and its representatives access to your clients to provide this service. This service costs $16.99 (sixteen dollars & ninety-nine cents), this service may cost less as a discount in different subscription packages.
    </p>
    <p>
        <u>Profit/Loss Assistant:</u> This service is where your client uses Bank Statement Assistant to sign into their Bank Accounts. Then Information sent over from Mastercard/Finicity is calculated and added into profit/loss per month from drop downs from your client(s) and/or you or your firm to complete the profit/loss. Bank Statement Assistant must be purchased to buy this service, which is included in the price. This service is an additional cost depending on the subscription you do or don’t choose starting at $45.99 (Forty-Five dollars and ninety-nine cents USD).
    </p>
    <p>
        <u>Client Storage:</u> The standard storage plan is for a total of 2 years (24 months). Premium, Premium Plus, Black Label & Ultimate subscriptions extend this time to 3 years (36 months) total storage time. 
    </p>
